# তুলনামূলক অপারেটরগুলো:
# == (সমান)
# != (সমান নয়)
# > (বড়)
# < (ছোট) 
# >= (বড় বা সমান)
# <= (ছোট বা সমান)

# এখানে সব ধরনের তুলনামূলক অপারেটর দেখানো হয়েছে

# সমান (==)
age = 18 

# বয়স ১৮ কিনা চেক করা
is_adult = age == 18
print("1. Is age 18? :", is_adult)

# সমান নয় (!=)
temperature = 25 

# তাপমাত্রা ২০ ডিগ্রি নয় কিনা
is_hot = temperature != 20 
print("2. Is temperature not 20 degrees? :", is_hot)

# বড় (>)
salary = 50000 

# বেতন ৪০০০০ টাকার বেশি কিনা
is_high_salary = salary > 40000 
print("3. Is salary more than 40000? :", is_high_salary)

# ছোট (<)
students = 25

# ক্লাসে ৩০ জনের কম ছাত্র আছে কিনা
is_small_class = students < 30  
print("4. Are there less than 30 students in class? :", is_small_class)

# বড় বা সমান (>=)
marks = 80

# প্রথম বিভাগ পেয়েছে কিনা
is_first_division = marks >= 80  
print("5. Got first division? :", is_first_division)

# ছোট বা সমান (<=)
weight = 65

# ওজন স্বাভাভিক সীমার মধ্যে কিনা
is_normal_weight = weight <= 70 
print("6. Is weight within normal range? :", is_normal_weight)