# পাইথনে অ্যারিথমেটিক অপারেটর (গাণিতিক অপারেটর)

# ডেমোনস্ট্রেশনের জন্য ভ্যারিয়েবল
a = 10
b = 3

# যোগ (+)
print(f"1. Addition: {a} + {b} = {a + b}")

# বিয়োগ (-)
print(f"2. Subtraction: {a} - {b} = {a - b}")

# গুণ (*)
print(f"3. Multiplication: {a} * {b} = {a * b}")

# ভাগ (/) - দশমিক ফলাফল
print(f"4. Division (Decimal): {a} / {b} = {a / b}")

# ফ্লোর ভাগ (//) - পূর্ণসংখ্যা ফলাফল
print(f"5. Division (Integer): {a} // {b} = {a // b}")

# মডুলাস (%) - ভাগশেষ
print(f"6. Remainder: {a} % {b} = {a % b}")

# এক্সপোনেনশিয়েশন (**) - সূচক/পাওয়ার
print(f"7. Exponentiation: {a} ** {b} = {a ** b}")

# কমপ্লেক্স অপারেশন উদাহরণ
print(f"\n8. Complex Operation Examples:")
print(f"9. ((({a} + {b}) * {a}) - {b}) = {(((a + b) * a) - b)}")
print(f"10. ({a} * {b}) + ({a} / {b}) = {(a * b) + (a / b)}")