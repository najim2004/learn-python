# অপারেটর প্রেসিডেন্স (Operator Precedence) হল অপারেটরগুলোর অগ্রাধিকার ক্রম
# নিচের উদাহরণগুলো দেখি:

# 1. গাণিতিক অপারেটর (Arithmetic Operators)
result1 = 2 + 3 * 4    # এখানে প্রথমে 3 * 4 = 12 হবে, তারপর 2 + 12 = 14
print("2 + 3 * 4 =", result1)  # আউটপুট: 14

# 2. ব্র্যাকেট দিয়ে অগ্রাধিকার পরিবর্তন
result2 = (2 + 3) * 4  # এখানে প্রথমে (2 + 3) = 5 হবে, তারপর 5 * 4 = 20
print("(2 + 3) * 4 =", result2)  # আউটপুট: 20

# অপারেটর প্রেসিডেন্স ক্রম (উপর থেকে নিচে):
# 1. () - ব্র্যাকেট
# 2. ** - এক্সপোনেনশিয়াল
# 3. +x, -x - ইউনারি প্লাস/মাইনাস
# 4. *, /, //, % - মাল্টিপ্লিকেশন, ডিভিশন, ফ্লোর ডিভিশন, মডুলাস
# 5. +, - - অ্যাডিশন, সাবট্রাকশন

# কিছু জটিল উদাহরণ:
complex_example = 2 ** 3 * 4 + 5 - 6 / 2
# ক্রম অনুযায়ী:
# 1. 2 ** 3 = 8
# 2. 8 * 4 = 32
# 3. 6 / 2 = 3
# 4. 32 + 5 = 37
# 5. 37 - 3 = 34
print("2 ** 3 * 4 + 5 - 6 / 2 =", complex_example)  # আউটপুট: 34.0